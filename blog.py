def heart_attack_blog():
    st.title("Understanding Heart Attacks: Risk Factors, Symptoms, and Prevention")
    st.write("""
        A heart attack, also known as a myocardial infarction, occurs when blood flow to a part of the heart is blocked for a long enough time that part of the heart muscle is damaged or dies. In this blog post, we'll explore the risk factors, symptoms, and prevention strategies for heart attacks.
        
        ### Risk Factors for Heart Attacks
        - **High Blood Pressure**: Also known as hypertension, high blood pressure is a leading cause of heart attacks.
        - **High Cholesterol**: Elevated levels of cholesterol in the blood can lead to the buildup of plaque in the arteries, increasing the risk of heart attacks.
        - **Smoking**: Tobacco use significantly increases the risk of heart attacks due to the harmful chemicals in cigarettes.
        - **Obesity**: Being overweight or obese puts extra strain on the heart and increases the likelihood of heart attacks.
        - **Family History**: Individuals with a family history of heart disease are at a higher risk of experiencing heart attacks.
        
        ### Symptoms of Heart Attacks
        Common symptoms of heart attacks include:
        - Chest pain or discomfort
        - Shortness of breath
        - Nausea or vomiting
        - Sweating
        - Pain or discomfort in the arms, back, neck, jaw, or stomach
        
        ### Prevention of Heart Attacks
        - **Healthy Diet**: A balanced diet low in saturated fats, cholesterol, and sodium can help reduce the risk of heart attacks.
        - **Regular Exercise**: Physical activity strengthens the heart muscle and improves overall cardiovascular health.
        - **Smoking Cessation**: Quitting smoking is one of the most effective ways to lower the risk of heart attacks.
        - **Control Blood Pressure and Cholesterol**: Managing high blood pressure and cholesterol levels through medication and lifestyle changes is essential for heart health.
        
        By understanding the risk factors, recognizing the symptoms, and adopting preventive measures, individuals can lower their risk of experiencing heart attacks and maintain a healthy heart.
    """)

