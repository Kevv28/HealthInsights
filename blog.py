import streamlit as st

def diabetes_blog():
    st.title("Understanding Diabetes: Causes, Symptoms, and Management")
    st.write("""
        Diabetes is a chronic condition that affects millions of people worldwide. It occurs when the body
        either does not produce enough insulin or cannot effectively use the insulin it produces. In this blog post,
        we'll explore the causes, symptoms, and management strategies for diabetes.
        
        ### Causes of Diabetes
        - **Type 1 Diabetes**: This type of diabetes is believed to be caused by an autoimmune reaction where the body's immune system attacks and destroys insulin-producing cells in the pancreas.
        - **Type 2 Diabetes**: This is the most common type of diabetes, often associated with lifestyle factors such as obesity, lack of physical activity, and poor diet.
        
        ### Symptoms of Diabetes
        Common symptoms of diabetes include:
        - Frequent urination
        - Increased thirst and hunger
        - Fatigue
        - Blurred vision
        - Slow healing of cuts and wounds
        
        ### Management of Diabetes
        - **Healthy Diet**: A balanced diet rich in fruits, vegetables, whole grains, and lean proteins can help manage blood sugar levels.
        - **Regular Exercise**: Physical activity helps improve insulin sensitivity and can lower blood sugar levels.
        - **Medication**: Some people with diabetes may require medication such as insulin injections or oral medications to manage their condition.
        
        Managing diabetes requires a holistic approach that includes lifestyle modifications, medication, and regular monitoring of blood sugar levels. By making healthy choices and working closely with healthcare professionals, individuals with diabetes can lead fulfilling lives.
    """)

